//Assignment operator
let x = 20; 

//addition assignment operator
x += 1; //take the variable x and add 1 to it
console.log(x);

//subtraction assignment operator
x -= 1; // x = x - 1
console.log(x);

//multiplication assignment operator
x *= 2; //x = x * 2
console.log(x);

//division assignment operator
x /= 4; //x = x / 4
console.log(x);

//remainder (modulus) assignment operator
x %= 2 //take x = x % 2
console.log(x);

//modulus operator
console.log(5%3); //5/3 is 1 with remainder 2
console.log(25%7); //25 divided by 7 is 3 with 4 remaining

//exponential assignment operator
x **= 2; 
console.log(x);

let y = 7; 
y **= 3; 
console.log(y);